void PrintImage(int[,] img)
{
    for (int i = 0; i < img.GetLength(0); i++)
    {
        for (int j = 0; j < img.GetLength(1); j++)
        {
            if (img[i, j] == 1)
            {
                Console.Write("+ ");
            }
            else
            {
                Console.Write("  ");
            }
        }
        Console.WriteLine(String.Empty);
    }
    Console.WriteLine(String.Empty);
}

void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col);
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}

